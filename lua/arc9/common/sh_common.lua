-- These could probably be defined in sh_utils.lua or something like that...
function ARC9.TimeToTicks(time)
	return math.floor(0.5 + (time / engine.TickInterval()))
end

function ARC9.TicksToTime(ticks)
	return engine.TickInterval() * ticks
end

function ARC9.RoundToTicks(time)
	return engine.TickInterval() * ARC9.TimeToTicks(time)
end
